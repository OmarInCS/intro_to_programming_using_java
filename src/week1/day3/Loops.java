package week1.day3;

public class Loops {

    public static void main(String[] args) {

        int x = 1;
        while (x <= 10) {
            System.out.print(x + "\t");
            x++;
        }
        System.out.println();
        System.out.println("--------------------");

        for (int y = 1; y <= 10; y++) {

            System.out.print(y + "\t");

        }

    }
}
